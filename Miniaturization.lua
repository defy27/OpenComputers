local robot = require("robot")

function placer()
	robot.down()
	robot.down()
	robot.select(1)
	robot.placeDown()
	robot.up()
	robot.placeDown()
	robot.up()
	robot.placeDown()
end

function build()
	placer()
	robot.forward()
	placer()
	robot.forward()
	placer()
	robot.turnRight()
	robot.forward()
	placer()
	robot.forward()
	placer()
	robot.turnRight()
	robot.forward()
	placer()
	robot.turnRight()
	robot.forward()
	placer()
	robot.forward()
	placer()
	robot.turnLeft()
	robot.forward()
	placer()
	robot.turnLeft()
	robot.forward()
	placer()
	robot.forward()
	placer()
end
